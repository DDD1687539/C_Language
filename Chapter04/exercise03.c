#include <stdio.h>

int main()
{
    printf("8 / 5 = %d\n", 8 / 5);          /* 1  */
    printf("-8 / 5 = %d\n", -8 / 5);        /* -1 */
    printf("8 / -5 = %d\n", 8 / -5);        /* -1 */
    printf("-8 / -5 = %d\n", -8 / -5);      /* 1  */
    return 0;
}

