#include <stdio.h>

int main()
{
    printf(" 8 %%  5 = %2d\n",  8 %  5);    // 3
    printf("-8 %%  5 = %2d\n", -8 %  5);    // -3
    printf(" 8 %% -5 = %2d\n",  8 % -5);    // 3
    printf("-8 %% -5 = %2d\n", -8 % -5);    // -3
    return 0;
}

